function mouseOn (){
    var a = document.getElementById("left-1");
    var b = document.getElementById("left-2");
    a.style.marginLeft = "-32px";
    a.style.transition = "all 0.7s";
    b.style.marginLeft = "-32px";
    b.style.transition = "all 0.7s";
    }
    function mouseOut (){
        var a = document.getElementById("left-1");
        var b = document.getElementById("left-2");
        a.style.marginLeft = "0";
        a.style.transition = "all 0.7s";
        b.style.marginLeft = "0";
        b.style.transition = "all 0.7s";
}
